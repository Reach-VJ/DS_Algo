package com.corejava;

public class PrintExample {
    public static void main(String [] args) {
        int x = 10, y = 20;
        String name = "Cutie";
        String nickName = "Pie";
        System.out.println(x);
        System.out.println(x+y); //Addition first and convert to string.
        System.out.println(x+" "+y);
        System.out.print(name+" "+nickName); //Print in same line
    }
}
